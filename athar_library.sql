CREATE DATABASE IF NOT EXISTS athar_library
  CHARACTER SET utf8mb4
  COLLATE utf8mb4_unicode_ci;

USE athar_library;

-- ============================================================
-- Table: books
-- ============================================================
CREATE TABLE IF NOT EXISTS books (
    id            INT UNSIGNED    NOT NULL AUTO_INCREMENT,
    book_number   SMALLINT UNSIGNED NOT NULL UNIQUE,   -- رقم الكتاب (1–56 etc.)
    title         VARCHAR(500)    NOT NULL,             -- الكتاب
    copies        TINYINT UNSIGNED NOT NULL DEFAULT 1, -- عدد النسخ
    is_available  TINYINT(1)      NOT NULL DEFAULT 1,  -- 1=متاح, 0=غير متاح
    created_at    TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP
                                          ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    INDEX idx_book_number  (book_number),
    INDEX idx_is_available (is_available)
) ENGINE=InnoDB
  DEFAULT CHARSET=utf8mb4
  COLLATE=utf8mb4_unicode_ci;

